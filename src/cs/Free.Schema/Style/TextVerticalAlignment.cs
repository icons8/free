namespace Free.Schema;
/// <summary>
/// Defines how a text is aligned vertically.
/// </summary>
public enum TextVerticalAlignment : byte
{
    /// <summary>
    /// Text is aligned to the top.
    /// </summary>
    Top = 0,
    /// <summary>
    /// Text is vertically centered.
    /// </summary>
    Middle = 1,
    /// <summary>
    /// Text is aligned to the bottom.
    /// </summary>
    Bottom = 2,
}