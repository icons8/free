namespace Free.Schema;
/// <summary>
/// Defines letter case. 
/// </summary>
public enum CharacterCasing : byte
{
    /// <summary>
    /// Normal letter case.
    /// </summary>
    Normal = 0,
    /// <summary>
    /// Upper case.
    /// </summary>
    UpperCase = 1,
    /// <summary>
    /// Lower case.
    /// </summary>
    LowerCase = 2,
    /// <summary>
    /// Title case.
    /// </summary>
    TitleCase = 3
}