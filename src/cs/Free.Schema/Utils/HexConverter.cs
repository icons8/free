using System.Runtime.CompilerServices;

namespace Free.Schema.Utils;

internal static class HexConverter
{
    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    public static byte FromDigit(byte digit) => (byte)(digit * 16 + digit);
    
    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    public static byte FromDigits(byte d1, byte d2) => (byte)(d1 * 16 + d2);

    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    public static byte DigitFromChar(char c) => c >= CharToHexLookup.Length ? (byte)0xFF : CharToHexLookup[c];

