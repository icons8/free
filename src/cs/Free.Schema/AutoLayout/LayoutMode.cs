namespace Free.Schema;
/// <summary>
/// Mode of an auto layout.
/// </summary>
public enum LayoutMode : byte
{
    /// <summary>
    /// Horizontal orientation.
    /// </summary>
    Horizontal = 0,
    /// <summary>
    /// Vertical orientation.
    /// </summary>
    Vertical = 1,
    /// <summary>
    /// Grid/Table mode.
    /// </summary>
    Grid = 2,
}